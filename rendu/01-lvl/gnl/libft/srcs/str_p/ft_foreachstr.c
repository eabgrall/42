#include "libft.h"

int			ft_foreachstr(char **tab, int (*f)(char *))
{
	int		i;

	i = 0;
	while (tab[i])
	{
		if (!((*f)(tab[i])))
			return (0);
		i++;
	}
	return (1);
}
