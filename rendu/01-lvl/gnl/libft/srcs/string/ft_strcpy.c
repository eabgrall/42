#include "libft.h"

char			*ft_strcpy(char *dest, const char *src)
{
	char		*tmpd;

	tmpd = dest;
	while (*src)
		*tmpd++ = *src++;
	if (!(*src))
		*tmpd++ = '\0';
	return (dest);
}
