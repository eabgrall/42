#include "libft.h"

t_div				ft_div(int n, int d)
{
	t_div			tmp;

	tmp.q = n / d;
	tmp.r = n - d * tmp.q;
	if (tmp.q < 0 && 0 < tmp.r)
	{
		tmp.q += 1;
		tmp.r -= d;
	}
	return (tmp);
}
